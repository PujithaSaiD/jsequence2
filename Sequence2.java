import java.util.*;
public class Sequence2 {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        List<String> s=new ArrayList<String>();
        for(int i=0;i<=n;i++){
            System.out.print(i);
        }
        for(int j=n-1;j>=0;j--){
            System.out.print(j);
            }
        
    }
}
